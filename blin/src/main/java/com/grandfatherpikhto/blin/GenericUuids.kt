/**
 * List from https://btprodspecificationrefs.blob.core.windows.net/assigned-values/16-bit%20UUID%20Numbers%20Document.pdf
 * The 16-bit UUID Numbers Document contains the following value types:
 * GATT Service,
 * GATT Unit,
 * GATT Declaration,
 * GATT Descriptor,
 * GATT Characteristic
 * and
 * Object Type,
 * 16-bit UUID for members,
 * Protocol Identifier,
 *.SDOGattService GATT Service,
 * Service Class and Profile.
 */
object GenericUuids {
    val baseUuid:ParcelUuid by lazy {
        ParcelUuid.fromString("00000000-0000-1000-8000-00805F9B34FB")
    }
    val genericAccess by lazy {
        ParcelUuid.fromString("00001800-0000-1000-8000-00805F9B34FB")
    }
    val genericAttribute by lazy {
        ParcelUuid.fromString("00001801-0000-1000-8000-00805F9B34FB")
    }

    const val TAG:String = "GenericUuids"

    fun genericName(uuidParcel: ParcelUuid, type: Type = Type.All):String? {
        return genericName(uuidParcel.uuid, type)
    }

    fun genericName(uuid: UUID, type: Type = Type.All):String? {
        if(uuid.isGeneric()) {
            return genericUuids.find {
                if(type != Type.All) {
                    it.uuid == uuid.to16()
                            && it.type == type
                } else {
                    it.uuid == uuid.to16()
                }
            }?.name
        }
        return uuid.toString()
    }

    data class UUID16(val type: Type, val uuid:Int, val name:String)
    enum class Type(val value:Int) {
        All(0x00),
        Member(0x01),
        Characteristic(0x02),
        Descriptor(0x03),
        Unit(0x04),
        SDOGattService(0x05),
        Protocol(0x06),
        ClassAndProfile(0x07),
        Service(0x08),
        Declaration(0x09)
    }

